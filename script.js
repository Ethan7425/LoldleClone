let difficulty = 'medium'; // Default difficulty
let correctAnswers = 0; // Correct answers counter
let isFullImageShown = false; // Track if the full image is currently shown
let points = 0;

// Update points on the point board
function updatePointsDisplay() {
    const pointsDisplay = document.getElementById("points");
    pointsDisplay.textContent = points;
}

// Modify points deduction to update the button state
function deductPoints(amount) {
  points = Math.max(points - amount, 0);
  updatePointsDisplay();
}


// Function to set the difficulty
function setDifficulty(level) {
  difficulty = level;

  // Highlight the active difficulty button
  const buttons = document.querySelectorAll("#difficulty-buttons button");
  buttons.forEach((button) => {
    button.classList.remove("active");
    if (button.textContent.toLowerCase() === level) {
      button.classList.add("active");
    }
  });

  // Reload an image based on the new difficulty
  loadRandomSkin();
}

function loadRandomSkin() {
  const randomIndex = Math.floor(Math.random() * skins.length);
  const selectedSkin = skins[randomIndex];

  const splash = document.getElementById("splash");
  splash.src = `skins/${selectedSkin.file}`;

  // Ensure champions are stored as an array
  const champions = Array.isArray(selectedSkin.champion)
    ? selectedSkin.champion
    : [selectedSkin.champion];

  splash.dataset.champion = JSON.stringify(champions); // Store champions as a JSON string
  splash.onerror = handleImageError; // Set error handler for missing images

  clearResult();
}



function clearResult() {
  // Clear the result message and input field after a small delay
  setTimeout(() => {
    // Clear the result message
    const result = document.getElementById("result");
    const corr_answer = document.getElementById("correct-answer");
    result.textContent = ""; // Reset the win/lose text
    result.style.color = ""; // Reset the text color
    result.style.display = "none"; // Hide the result box
    corr_answer.textContent = "";
    // Clear the input field
    const guessInput = document.getElementById("guess-input");
    guessInput.value = ""; // Reset the input text
  }, 1000); // 1000 milliseconds = 1 second delay
}


function toggleFullImage() {
  const fullSplashContainer = document.getElementById("full-splash-container");
  const splash = document.getElementById("splash");
  const fullSplash = document.getElementById("full-splash");
  const toggleButton = document.getElementById("toggle-full-image");
  const toggleButtonContainer = document.getElementById("toggle-full-image-container");

  if (fullSplashContainer.style.display === "none" || !fullSplashContainer.style.display) {
    // Show the full image
    fullSplash.src = splash.src;
    fullSplashContainer.style.display = "flex";
    toggleButton.textContent = "Hide Full Image";

    // Add class to move the button down
    toggleButtonContainer.classList.add("show-full");
  } else {
    // Hide the full image
    fullSplashContainer.style.display = "none";
    toggleButton.textContent = "Show Full Image";

    // Remove class to reset the button position
    toggleButtonContainer.classList.remove("show-full");
  }
}

function checkGuess() {
  const splash = document.getElementById("splash");
  const correctAnswer = JSON.parse(splash.dataset.champion); // Parse the JSON string into an array
  const userGuess = document.getElementById("guess-input").value.trim().toLowerCase();
  const result = document.getElementById("result"); // The result box
  const correctAnswerBox = document.getElementById("correct-answer"); // Correct answer display

  if (correctAnswer.includes(userGuess)) {
    // User's guess matches any correct answer
    result.textContent = "Correct! You guessed the champion!";
    result.className = "correct"; // Add the "correct" class for green styling
    result.style.display = "block"; // Ensure the result box is visible
    correctAnswerBox.textContent = ""; // Clear the correct answer message
    correctAnswers++; // Increment correct answers count
    updateScore(); // Update score counter
    // loadRandomSkin(); // Load a new skin
    points += 1000; // Add points
    updatePointsDisplay(); // Update points display
    
    // Show the full art popup
    showFullArtPopup(splash.src);
  } else {
    // Incorrect guess
    deductPoints(250); // Deduct points for wrong answer
    result.textContent = "Incorrect. Try again!";
    result.className = "incorrect"; // Add the "incorrect" class for red styling
    result.style.display = "block"; // Ensure the result box is visible
    // correctAnswerBox.textContent = `The correct answer was: ${correctAnswer.join(", ").toUpperCase()}`; // Show all correct answers
    clearResult(); // Clear the result after a delay
  }
}


function showFullArtPopup(imageSrc) {
  const popup = document.getElementById("full-art-popup");
  const fullArtImage = document.getElementById("full-art-image");
  const nextGuessBtn = document.getElementById("next-guess-btn");

  // Set the image source for the full art
  fullArtImage.src = imageSrc;

  // Show the popup
  popup.style.display = "block";

  // Add event listener to the "Next Guess" button
  nextGuessBtn.addEventListener("click", function() {
    // Hide the popup and reset the input field
    popup.style.display = "none";
    document.getElementById("guess-input").value = ""; // Reset the input field
    loadRandomSkin();
  });
}


// Function to update the score display
function updateScore() {
  const scoreCounter = document.getElementById("correct-count");
  scoreCounter.textContent = correctAnswers;
}

// Function to start a new game
function startNewGame() {
  points = 0; // Reset points
  updatePointsDisplay();
  correctAnswers = 0; // Reset the correct answers counter
  updateScore(); // Update the score display
  loadRandomSkin(); // Load a new skin
  const result = document.getElementById("result");
  result.textContent = ""; // Clear the result message
  const guessInput = document.getElementById("guess-input");
  guessInput.value = ""; // Clear the input field
}

function tryCropSplash() {
  if (points < 50) {
    alert("Not enough points to recrop!"); // Notify the player
    return;
  }
  deductPoints(50);
  cropSplashRandomly();
}

// Function to crop the splash randomly based on difficulty
function cropSplashRandomly() {
  const container = document.getElementById("splash-container");
  const splash = document.getElementById("splash");

  const containerWidth = container.offsetWidth;
  const containerHeight = container.offsetHeight;

  // Set zoom levels based on difficulty
  let zoomLevel;
  if (difficulty === 'easy') {
    zoomLevel = 2.5; // Slight zoom-out for easy
  } else if (difficulty === 'medium') {
    zoomLevel = 3.0; // Moderate zoom-in for medium
  } else if (difficulty === 'hard') {
    zoomLevel = 4.5; // Significant zoom-in for hard
  }

  // Calculate new image size based on zoom level
  const splashWidth = containerWidth * zoomLevel;
  const splashHeight = containerHeight * zoomLevel;

  // Position the image randomly within the crop bounds
  const maxOffsetX = splashWidth - containerWidth;
  const maxOffsetY = splashHeight - containerHeight;

  const randomX = -Math.random() * maxOffsetX;
  const randomY = -Math.random() * maxOffsetY;

  splash.style.width = `${splashWidth}px`;
  splash.style.height = `${splashHeight}px`;
  splash.style.left = `${randomX}px`;
  splash.style.top = `${randomY}px`;
}

// Function to handle image load errors and load a new random skin
function handleImageError() {
  console.warn(`Image not found: ${this.src}. Loading a new random skin.`);
  loadRandomSkin(); // Load another random skin
}

// Initialize the game
document.addEventListener("DOMContentLoaded", () => {
  setDifficulty('medium'); // Set default difficulty
  loadRandomSkin(); // Load a random skin
  updatePointsDisplay();
  document.getElementById("splash").onload = cropSplashRandomly; // Crop after image loads

  // Add event listener to trigger guess on Enter key press
  const guessInput = document.getElementById("guess-input");
  const guessForm = document.getElementById("guess-form");

  // Trigger the guess when Enter key is pressed in the input field
  guessInput.addEventListener("keydown", function(event) {
    if (event.key === "Enter") {
      event.preventDefault(); // Prevent form submission
      checkGuess(); // Call checkGuess() on Enter key press
    }
  });
});



